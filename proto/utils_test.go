package proto
import "testing"

func TestChecksum( tester * testing.T ) {
	var chksum uint16
	chksum = Checksum( [ ]byte{ 0x45 , 0x00 , 0x00 , 0x73 , 0x00 , 0x00 , 0x40 , 0x00 , 0x40 , 0x11 , 0x00 , 0x00 , 0xC0 , 0xA8 , 0x00 , 0x01 , 0xC0 , 0xA8 , 0x00 , 0xC7 } )
	if chksum != 0xB861 {
		tester.Fatalf( "%X\r\n" , chksum )
		return
	}
}

